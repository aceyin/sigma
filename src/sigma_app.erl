-module(sigma_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(Type, Args) ->
  io:format("sigma_app start/2 called, args[1]:~p, args[2]:~p~n", [Type, Args]),
  {ok, Sup} = sigma_sup:start_link(),
  network_app:start([]),
  {ok, Sup}.

%%--------------------------------------------------------------------
stop(_State) ->
  ok.

